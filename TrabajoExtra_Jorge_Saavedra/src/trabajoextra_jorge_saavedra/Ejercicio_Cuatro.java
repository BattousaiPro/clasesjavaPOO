    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (!ValidarCamposObligatorios()) {
            Producto prod = new Producto(
                    jTextField1.getText().trim(),
                    Double.parseDouble(jTextField2.getText().trim()),
                    jTextField3.getText().trim()
            );
            productos.add(prod);
            this.jTable1.setModel(new DefaultTableModel(null, new String[]{"Nro", "Nombre", "Precio", "Categoría"}));
            for (int i = 0; i < productos.size(); i++) {
                DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();

                model.addRow(new Object[]{
                    i + 1,
                    productos.get(i).getNombre(),
                    productos.get(i).getPrecio(),
                    productos.get(i).getCategoria()
                });
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

